#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	size_t	nb;
	char	c;

	if (nb == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	nb = n % 10;
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	c = nb + '0';
	write(fd, &c, 1);
}
